CREATE OR REPLACE FUNCTION HCD_OBTER_MAN_RESULT_AVAL (NR_SEQ_AVAL_P NUMBER, NR_SEQ_ITEM_P NUMBER)
RETURN VARCHAR2 IS

/*<DS_SCRIPT>
 DESCRIÇÃO...: Function que retorna o resultado de uma avaliação gerada apartir da função Ordem de Serviço
 RESPONSAVEL.: DOUGLAS FELIPE (douglas.felipe)
 DATA........: 25/04/2023
 APLICAÇÃO...: TASY (Ordem Serviço(Nova) --> Avaliação )
 ATUALIZAÇÃO...:
</DS_SCRIPT>*/



DS_RETORNO VARCHAR2(255);

BEGIN

SELECT DS_RESULTADO 
    INTO DS_RETORNO
FROM MAN_ORDEM_SERV_AVAL_RESULT 
WHERE NR_SEQ_ORDEM_SERV_AVAL = NR_SEQ_AVAL_P AND NR_SEQ_ITEM = NR_SEQ_ITEM_P;


RETURN DS_RETORNO;

END HCD_OBTER_MAN_RESULT_AVAL;